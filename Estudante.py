class Estudante:

    def  __INIT__(self, raAluno, nomeAluno, curso, ingresso):
        self.raAluno = raAluno
        self.nomeAluno = nomeAluno
        self.curso = curso
        self.ingresso = ingresso


    def getRaAluno(self):
        return self.raAluno


    def setRaAluno(self, raAluno):
        self.raAluno=raAluno


